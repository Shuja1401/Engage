def run_fetch():
    conn = psycopg2.connect(DB_URL)
    cur  = conn.cursor()

    now_ist    = datetime.now(IST_OFFSET)
    noon_today = now_ist.replace(hour=CAPTURE_HOUR, minute=0, second=0, microsecond=0)

    published_before_dt = noon_today - timedelta(days=1)
    published_after_dt  = published_before_dt - timedelta(days=LOOKBACK_DAYS)
    fetched_on          = now_ist.date().isoformat()

    print(f"Fetch window : {published_after_dt.isoformat()} → {published_before_dt.isoformat()}")
    print(f"Fetched on   : {fetched_on}\n")

    cur.execute("SELECT channel_id, name, category FROM channels")
    channels = cur.fetchall()

    total_videos    = 0
    total_snapshots = 0

    for channel_id, name, category in channels:
        print(f"→ {name} ({category})", end=" ... ", flush=True)
        t0 = time.time()

        try:
            video_ids = get_channel_videos(channel_id, published_after_dt, published_before_dt)

            if not video_ids:
                print("0 videos")
                continue

            details = get_video_details(video_ids)

            batch_upsert_videos(cur, details, category)
            batch_insert_snapshots(cur, details, fetched_on)
            conn.commit()

            elapsed = round(time.time() - t0, 1)
            print(f"{len(details)} videos — {elapsed}s")

            total_videos    += len(details)
            total_snapshots += len(details)

        except HttpError as e:
            print(f"API ERROR {e.resp.status}: {e.error_details}")
            if e.resp.status == 403:
                print("!! Quota exhausted — aborting.")
                break
            continue

        except Exception as e:
            print(f"ERROR: {e}")
            continue

    # Sync denormalized metrics
    cur.execute("""
        UPDATE videos
        SET
            views    = (SELECT MAX(views)    FROM video_snapshots WHERE video_snapshots.video_id = videos.video_id),
            likes    = (SELECT MAX(likes)    FROM video_snapshots WHERE video_snapshots.video_id = videos.video_id),
            comments = (SELECT MAX(comments) FROM video_snapshots WHERE video_snapshots.video_id = videos.video_id)
        WHERE video_id IN (SELECT DISTINCT video_id FROM video_snapshots)
    """)
    conn.commit()

    print(f"""
─────────────────────────────
Fetch complete — {fetched_on}
  Videos processed : {total_videos}
  Snapshots written: {total_snapshots}
─────────────────────────────""")

    cur.close()
    conn.close()


if __name__ == "__main__":
    run_fetch()
